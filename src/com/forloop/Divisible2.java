package com.forloop;
//write a code to display numbers between 1 to 20 if the number is divisible by 3 & 5 then skip the numbers. 

public class Divisible2 {

	public static void main(String[] args) {
		for(int i=1; i<=20; i++)
		{
			if(i%3!=0 && i%5!=0)
			{
				
				System.out.println(i);
				
				
			}
		
			
		}

	}

}
